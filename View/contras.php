            <p>
                A Universidade Federal do Rio de Janeiro (UFRJ) também se posicionou por meio de um documento produzido após análise da proposta do programa Future-se do Ministério da Educação. No documento, a UFRJ aponta os riscos no Future-se, sendo um deles à possibilidade de mudança futura da personalidade jurídica das Ifes (Instituições Federais de Ensino Superior). Abaixo encontra-se o texto na íntegra do documento publicado pela UFRJ.
            </p>
        
            <p>
                O Ministério da Educação apresentou para consulta pública o programa Future-se, que teria como objetivo o fortalecimento da autonomia administrativa, financeira e de gestão das Instituições Federais de Ensino Superior (Ifes). A abrangência do programa se refere a três eixos:
            </p>
            <p>
            O Ministério da Educação apresentou para consulta pública o programa Future-se, que teria como objetivo o fortalecimento da autonomia administrativa, financeira e de gestão das Instituições Federais de Ensino Superior (Ifes). A abrangência do programa se refere a três eixos:<br>
1) Gestão, governança e empreendedorismo;<br>
2) Pesquisa e inovação;<br>
3) Internacionalização.

            </p>
            <p>
            Algumas premissas devem ser consideradas cláusulas pétreas que antecedem a possibilidade de adesão a esse ou qualquer outro programa de governo:<br>
1) Garantia da autonomia universitária estabelecida pelo artigo 207 da Constituição brasileira e do caráter público e gratuito do ensino superior;<br>
2) Defesa da integralidade da Universidade, evitando a fragmentação da sua estrutura e a indissociabilidade entre ensino, pesquisa e extensão;<br>
3) Garantia de financiamento público adequado para a manutenção e funcionamento das Ifes;<br>
4) Flexibilização dos limites de captação e uso dos recursos próprios captados pelas Ifes;<br>
5) Garantia de preservação das carreiras públicas nas Ifes, com a manutenção dos concursos públicos e da contratação via Regime Jurídico Único, da estabilidade e também, no caso dos professores, a preservação da dedicação exclusiva.

            </p>
            <p>O artigo 207 da constituição brasileira de 1988 garante às universidades a autonomia didático-científica, administrativa e de gestão financeira e patrimonial, e que obedecerão ao princípio de indissociabilidade entre ensino, pesquisa e extensão. Importante ressaltar que o dispositivo não desincumbe o Estado da provisão de financiamento público, mastão somente garante a autonomia da gestão financeira, a partir de recursos públicos. Destaca-se que é antecedido pelo artigo 206 que explicitamente menciona a gratuidade do ensino em instituições públicas. A provisão pública de recursos é, assim, prevista e uma necessidade para o funcionamento das Ifes. </p>
            <p>Em linhas gerais, pelo que se extrai do Programa Future-se, a Ifes que lhe fizer a adesão comprometer-se-á com pelo menos uma organização social a ser contratada, emoldurando sua atuação com as diretrizes de governança que serão futuramente definidas pelo Ministério da Educação. No mesmo sentido, deverá aderir a um programa de integridade, mapeamento e gestão de riscos corporativos, controle interno e auditoria externa. Por essa perspectiva, ressalte-se, as Ifes já são regularmente auditadas por órgãos internos e externos de controle. Os contratos de gestão seriam firmados pela União e pela Ifes com uma organização social (OS), cujas atividades seriam dirigidas ao ensino, à pesquisa científica, ao desenvolvimento tecnológico, à cultura e estariam relacionadas às finalidades do Programa. </p>
            <p>Para dar cumprimento ao contrato de gestão, a União e as Ifes poderão fomentar a organização social por meio de repasse de recursos orçamentários, cessão de servidores e permissão de uso de bens públicos, o que pode suscitar questionamentos acerca da finalidade estrita da transferência de recursos públicos a entidades privadas. São excessivamente vagas as atribuições, competências e limites das Organizações Sociais neste Programa. </p>
            <p>
            Não está explicitamente definida a possibilidade de essas entidades atuarem nas atividades-fim das Ifes. A flagrante ausência de clareza nesse aspecto depõe contra uma análise percuciente sobre as finalidades do Programa, que parece merecer reparo para um debate que leve à conclusão sobre sua eficiência, eficácia e efetividade no âmbito das Ifes. Outro aspecto que merece destaque é a previsão da criação de um comitê gestor cuja composição não está definida na proposta. No entanto, está descrito que ao comitê gestor competirá: <br>
             I)Estabelecer as diretrizes das ações no âmbito do Programa;<br>
            II)realizar avaliação anual de desempenho institucional, conforme disposto em regulamento, para análise do atingimento dos objetivos e metas pactuados no Plano de Ação; <br>
            III)assessorar as Ifes e organizações sociais participantes na condução da política de governança e transparência; <br> 
            IV) garantir a estrita observância dos limites de gasto com pessoal;<br>  V) assegurar a correta e regular destinação dos recursos do programa. 
            </p>
            <p>A autonomia universitária poderá ser afetada pela presença deste comitê externo que parece substituir a função dos colegiados superiores das universidades. Pela sua configuração atual, o Future-se não se apresenta disposto a promover o fortalecimento da autonomia universitária. Contrario sensu, pode indicar retrocesso aos avanços do ordenamento jurídico pátrio garantidores das melhores perspectivas para o desenvolvimento socioeconômico, científico e cultural do país, que emergem das Ifes</p>
            <p>Em outros termos, as disposições da proposta do Programa, nesse contexto original, tendem a romper o invólucro constitucional que protege a autonomia administrativa, didática e de gestão financeira das Ifes, que passaria a depender e ser gerida pela OS. O chamado Fundo do Conhecimento, proposto no programa, é também recheado de lacunas. Não há clareza sobre a composição do patrimônio que serviria de aporte inicial, não se discutem o tempo de maturação de um fundo deste tipo e como as Ifes seriam financiadas durante esta transição, não há qualquer menção aos critérios de escolha do gestor do referido fundo e de como ele será remunerado, especialmente no período de lançamento e consolidação do fundo. Caso o fundo fracasse, o retorno do patrimônio é previsto ao MEC, sem esclarecer com ficam os aportes eventualmente feitos pelas Ifes. Diante desse cenário, há que se reafirmar a necessidade de políticas que possam garantir o repasse completo do orçamento da Ifes previsto e aprovado pela lei orçamentária anual (LOA), o que permitiria o planejamento e funcionamento de forma mais eficiente das instituições. Além disso, deve-se permitir o uso total dos recursos próprios captados por meio de boas práticas de gestão, inclusive patrimonial. Essas ações seriam suficientes para garantir a autonomia de gestão financeira. Em conclusão, a área de educação pública não deveria ser atingida pelo teto de gastos imposto pela EC 95, sob a pena de não sobreviver nos próximos anos. O intuito deveria ser o de estimular as boas práticas de gestão financeira e patrimonial das Ifes, aumentar o financiamento das instituições públicas e não desviá-lo para uma organização privada, a OS. Jamais será possível prescindir do orçamento público adequado para o funcionamento e o integral cumprimento da missão das Ifes.</p>
            <p>Nessa linha, merece destaque o fato de as Ifes ostentarem com pujança a competência necessária para pensar e desenvolver suas atividades, sem necessidade de concepção de fórmulas jurídicas, carentes de comprovação fática de eficiência, para delegar à OS sua gestão, quando, de outro lado, permanecem as fundações de apoio legalmente consolidadas na gestão dos projetos de pesquisa, desenvolvimento e inovação, há décadas. A defesa da autonomia financeira das Ifes deve pautar o debate, nos moldes do que já está regulamentado e funcionando em algumas universidades estaduais brasileiras desde 1989, garantindo o investimento em educação, pesquisa e desenvolvimento de maneira mais sistemática. A garantia de financiamento público permite que a Ifes proceda com o adequado planejamento e as melhores práticas de governança e compliance.</p>
            <p>Certamente, o projeto Future-se deveria propor a retirada das Ifes do teto de gastos, garantir o orçamento sem contingenciamentos e estimular o desenvolvimento para impulsionar a produção científica nacional e a formação de pessoal qualificada. Estamos abertos ao diálogo permanente para fortalecer o ensino público, gratuito e de qualidade; porém, concluímos que há riscos no Programa Future-se relacionados à possibilidade de mudança futura da personalidade jurídica das Ifes, que são atualmente autarquias federais com a prerrogativa do autogoverno, além dos riscos à nossa integridade administrativa, pedagógica, científica e patrimonial. É nosso entendimento que o debate contínuo com os poderes federativos é capaz de constituir uma proposta que atenda as necessidades e o desenvolvimento efetivo das Ifes. Mas os princípios que elencamos devem estar assegurados, o que não é o caso nos termos propostos no Future- se. Ao contrário, o programa aponta numa direção diversa que não aborda os problemas centrais do financiamento do ensino superior em um contexto de profundo desgaste para as Ifes submetidas à profunda instabilidade orçamentária.</p>
            <span>Reitoria da UFRJ.</span>
